template < typename T >
electrical::TwoPort< myMatrixType > *CreateElectricalElement()
{
    boost::shared_ptr< object::Object< double > > dummyObj;    // <--- deref will make boom
    return new T( dummyObj );
}

template < typename T >
electrical::TwoPortWithChild< myMatrixType > *CreateTwoPortWithChildren()
{
    return new T();
}


electrical::TwoPort< myMatrixType > *CreateComplexExample()
{
    electrical::TwoPortWithChild< myMatrixType > *mainSerialTwoPort(
     CreateTwoPortWithChildren< electrical::SerialTwoPort< myMatrixType > >() );

    electrical::TwoPortWithChild< myMatrixType > *twoPort(
     CreateTwoPortWithChildren< electrical::ParallelTwoPort< myMatrixType > >() );
    electrical::TwoPortWithChild< myMatrixType > *serialTwoPort(
     CreateTwoPortWithChildren< electrical::SerialTwoPort< myMatrixType > >() );
    electrical::TwoPortWithChild< myMatrixType > *parallelTwoPort(
     CreateTwoPortWithChildren< electrical::ParallelTwoPort< myMatrixType > >() );
    parallelTwoPort->AddChild( CreateElectricalElement< electrical::OhmicResistance< myMatrixType > >() );
    parallelTwoPort->AddChild( CreateElectricalElement< electrical::OhmicResistance< myMatrixType > >() );

    serialTwoPort->AddChild( CreateElectricalElement< electrical::OhmicResistance< myMatrixType > >() );
    serialTwoPort->AddChild( parallelTwoPort );
    serialTwoPort->AddChild( CreateElectricalElement< electrical::OhmicResistance< myMatrixType > >() );
    twoPort->AddChild( serialTwoPort );

    serialTwoPort = CreateTwoPortWithChildren< electrical::SerialTwoPort< myMatrixType > >();
    for ( size_t i = 0; i < 3; ++i )
        serialTwoPort->AddChild( CreateElectricalElement< electrical::OhmicResistance< myMatrixType > >() );
    twoPort->AddChild( serialTwoPort );
    mainSerialTwoPort->AddChild( twoPort );

    mainSerialTwoPort->AddChild( CreateElectricalElement< electrical::OhmicResistance< myMatrixType > >() );

    parallelTwoPort = CreateTwoPortWithChildren< electrical::ParallelTwoPort< myMatrixType > >();
    for ( size_t i = 0; i < 4; ++i )
        parallelTwoPort->AddChild( CreateElectricalElement< electrical::OhmicResistance< myMatrixType > >() );
    mainSerialTwoPort->AddChild( parallelTwoPort );

    return mainSerialTwoPort;
}

boost::shared_ptr< electrical::TwoPort< myMatrixType > > GenerateNetworkFromFile( const char *fileName )
{
    boost::scoped_ptr< factory::Factory< ::state::Dgl_state, factory::ArgumentTypeState > > stateFactory( factory::BuildStateFactory() );
    boost::scoped_ptr< factory::Factory< object::Object< double >, factory::ArgumentTypeObject< double > > > objectFactory(
     factory::BuildObjectFactory< double >( stateFactory.get() ) );
    boost::scoped_ptr< factory::Factory< electrical::TwoPort< myMatrixType >, factory::ArgumentTypeElectrical > > electricalFactory(
     factory::BuildElectricalFactory< myMatrixType, double >( objectFactory.get(), stateFactory.get() ) );

    boost::scoped_ptr< xmlparser::XmlParser > parser( new xmlparser::tinyxml2::XmlParserImpl() );
    parser->ReadFromFile( fileName );

    boost::shared_ptr< xmlparser::XmlParameter > rootParam( parser->GetRoot()->GetElementChild( "RootElement" ) );
    boost::shared_ptr< electrical::TwoPort< myMatrixType > > rootPort( electricalFactory->CreateInstance( rootParam ) );
    return rootPort;
}

